/*
 ## Operadores Aritmeticos 
*/

// Multiplicação *
    console.log(3.2 * 5.5)

// Divisão /
    console.log(12 / 2)

// Soma +
    console.log(50 + 70)

// Subtração -
    console.log(50 - 30)

// Resto da Divisão %
    let remainder
    remainder = 11 % 10
    console.log(remainder)

// Incremento ++
    let increment
    increment = 0
    ++increment
    console.log(increment)

// Decremento --
    let decrement
    decrement = 0
    decrement--
    console.log(decrement)

// Exponencial **
    console.log(3 ** 2)
    