/*
  Type conversion (typecasting) vs Type coersion

    * Alteração de um tipo de dado para outro tipo
*/

// Ex:
console.log('9' + 5) /* Aqui o JS faz uma coersion ou seja ele 
transforma(força) um numero para uma String. */

console.log(Number('9') + 5) /* Já nesse exemplo houve uma conversion(converteu)
 uma string para number */
